export const getCountryFlag = (countryName) => {
  if (!countryName) return '❓';

  // Try exact match first
  if (countryFlags[countryName]) {
    return countryFlags[countryName];
  }

  // Try case-insensitive match
  const normalized = Object.keys(countryFlags).find(
    key => key.toLowerCase() === countryName.toLowerCase()
  );

  if (normalized) {
    return countryFlags[normalized];
  }

  // Try partial match for longer names
  const partialMatch = Object.keys(countryFlags).find(
    key => countryName.toLowerCase().includes(key.toLowerCase()) ||
            key.toLowerCase().includes(countryName.toLowerCase())
  );

  if (partialMatch) {
    return countryFlags[partialMatch];
  }

  // Fallback
  return '🏳️';
};
